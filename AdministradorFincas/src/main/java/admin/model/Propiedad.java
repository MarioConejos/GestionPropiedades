	public void setPorcentajeGastosComunidad(String porcentajeGastosComunidad) {
		this.porcentajeGastosComunidad = porcentajeGastosComunidad;
	}
	public String getCuentaDomiciliadaRecibos() {
		return cuentaDomiciliadaRecibos;
	}
	public void setCuentaDomiciliadaRecibos(String cuentaDomiciliadaRecibos) {
		this.cuentaDomiciliadaRecibos = cuentaDomiciliadaRecibos;
	}
	

